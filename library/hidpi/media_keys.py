"""
Media (Consumer Control) key bit masks.
"""

MEDIA_VOLUME_UP      = 1 << 0  # Usage 0xE9
MEDIA_VOLUME_DOWN    = 1 << 1  # Usage 0xEA
MEDIA_MUTE           = 1 << 2  # Usage 0xE2
MEDIA_PLAY           = 1 << 3  # Usage 0xB0
MEDIA_PAUSE          = 1 << 4  # Usage 0xB1
MEDIA_NEXT_TRACK     = 1 << 5  # Usage 0xB5
MEDIA_PREV_TRACK     = 1 << 6  # Usage 0xB6
MEDIA_STOP           = 1 << 7  # Usage 0xB7

